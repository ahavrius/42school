#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*c;

	i = 0;
	while (src[i] != '\0')
		i++;
	c = malloc(i + 1);
	i = -1;
	while (src[++i] != '\0')
		c[i] = src[i];
	c[i] = '\0';
	return (c);
}
